"""Единицы измерения и физические константы — единая точка истины.

Все переводы единиц и базовые физические соотношения собраны здесь, чтобы
расчётное ядро нигде не «зашивало» множители. Каждая функция прокомментирована
со ссылкой на формулу/единицы из Методики энергоаудита (разд. 8).

Соглашения по единицам в ядре:
    давление p      — МПа
    расход   Q      — м³/ч
    напор    H      — м
    плотность ρ     — кг/м³
    мощность P      — кВт
    энергия  W      — кВт·ч
    УРЭ             — кВт·ч/м³
"""

from __future__ import annotations

# --- Физические константы -------------------------------------------------

G = 9.81  # ускорение свободного падения, м/с² (Методика, разд. 8)

# Перевод технической атмосферы (кгс/см²) в МПа.
# Методика предписывает множитель 0,098 (точное значение 0,0980665).
KGFCM2_TO_MPA = 0.098
MPA_TO_KGFCM2 = 1.0 / KGFCM2_TO_MPA

# Коэффициент гидравлической мощности: P[кВт] = Δp[МПа]·Q[м³/ч] / 3.6
# Вывод: P[Вт] = Δp[Па]·Q[м³/с] = (Δp·1e6)·(Q/3600) = Δp·Q·1e6/3600;
#        в кВт делим на 1000 → Δp·Q·(1e6/3600/1e3) = Δp·Q/3.6.
HYDRAULIC_POWER_DIVISOR = 3.6


# --- Переводы единиц ------------------------------------------------------

def kgfcm2_to_mpa(p_kgfcm2: float) -> float:
    """кгс/см² → МПа (Методика, разд. 8: множитель 0,098)."""
    return p_kgfcm2 * KGFCM2_TO_MPA


def mpa_to_kgfcm2(p_mpa: float) -> float:
    """МПа → кгс/см²."""
    return p_mpa * MPA_TO_KGFCM2


def mm_to_m(x_mm: float) -> float:
    """мм → м (для уровней БЕ, геометрии трассы)."""
    return x_mm / 1000.0


# --- Базовые гидравлические соотношения -----------------------------------

def head_from_pressure(dp_mpa: float, rho: float) -> float:
    """Напор по перепаду давления, м.

    H = Δp · 10⁶ / (ρ · g)   — формула (8)/(10) Методики,
    где Δp в МПа (→ Па через ×10⁶), ρ в кг/м³.
    """
    return dp_mpa * 1.0e6 / (rho * G)


def hydraulic_power_kw(dp_mpa: float, q_m3h: float) -> float:
    """Гидравлическая мощность, кВт.

    P_гидр = Δp · Q / 3.6   — формула (11) Методики,
    Δp в МПа, Q в м³/ч → результат в кВт.
    """
    return dp_mpa * q_m3h / HYDRAULIC_POWER_DIVISOR
